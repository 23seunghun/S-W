for i in range(10): #반복 조건
    print(i,end=' ') #반복조건이 참인 동안 반복

print()

for  k in range(1,10): #반복 조건
    print(k,end=' ')

print()

for j in range(1,11,2):
    print(j,end=' ')

for num in range(10,0,-1):
    print(num,end=' ')